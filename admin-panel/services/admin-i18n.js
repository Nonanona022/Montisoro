  var LS_KEY = 'montisoro_admin_lang';

  function getLang() {
    try {
      var v = localStorage.getItem(LS_KEY);
      if (v === 'en' || v === 'nl') return v;
    } catch (e) {}
    return 'nl';
  }

  function setLang(lang) {
    lang = (lang === 'en') ? 'en' : 'nl';
    try { localStorage.setItem(LS_KEY, lang); } catch (e) {}
    document.documentElement.setAttribute('lang', lang);
    apply(document);
    document.dispatchEvent(new CustomEvent('admin:langchange', { detail: { lang: lang } }));
    return lang;
  }

  /* t('key')            -> current language
     t('key', 'en')      -> explicit language
     Falls back to NL, then to the raw key, so nothing ever renders blank. */
  function t(key, lang) {
    lang = lang || getLang();
    var bank = STRINGS[lang] || STRINGS.nl;
    if (bank[key] != null) return bank[key];
    if (STRINGS.nl[key] != null) return STRINGS.nl[key];
    return key;
  }

  /* Swap every element carrying:
       data-i18n           -> textContent
       data-i18n-ph        -> placeholder
       data-i18n-title     -> title
       data-i18n-aria      -> aria-label
     Safe to call repeatedly (idempotent). */
  function apply(scope) {
    scope = scope || document;
    var lang = getLang();
    scope.querySelectorAll('[data-i18n]').forEach(function (el) {
      var k = el.getAttribute('data-i18n'), v = t(k, lang);
      if (v !== k) el.textContent = v; /* audit P4 — ontbrekende sleutel: behoud bestaande tekst i.p.v. raw key */
    });
    scope.querySelectorAll('[data-i18n-html]').forEach(function (el) {
      el.innerHTML = t(el.getAttribute('data-i18n-html'), lang);
    });
    scope.querySelectorAll('[data-i18n-ph]').forEach(function (el) {
      el.setAttribute('placeholder', t(el.getAttribute('data-i18n-ph'), lang));
    });
    scope.querySelectorAll('[data-i18n-title]').forEach(function (el) {
      el.setAttribute('title', t(el.getAttribute('data-i18n-title'), lang));
    });
    scope.querySelectorAll('[data-i18n-aria]').forEach(function (el) {
      el.setAttribute('aria-label', t(el.getAttribute('data-i18n-aria'), lang));
    });
  }

  return {
    strings: STRINGS,
    t: t,
    getLang: getLang,
    setLang: setLang,
    apply: apply
  };
